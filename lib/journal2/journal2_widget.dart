/// Goal: Build a minimalist Journal page for Calibi Sync.
///
/// Users write today’s entry with a mood dropdown, and browse past entries by
/// selecting Month and Year from dropdowns (no calendar). When a month/year
/// is selected, entries for that month appear below.
/// Data (Firestore)
/// Collection: users/{authUser.uid}/journalEntriesFields:
/// * date (Timestamp, start of day)
/// * text (String)
/// * mood (String: “Great”, “Good”, “Okay”, “Low”, “Drained”)
/// * createdAt (Timestamp, server)
/// * updatedAt (Timestamp, server)
/// Index: order by date DESC (single-field is fine). If you add filters, keep
/// orderBy date.
/// Page
/// Name: JournalPageSimpleFiltersTheme: White background; Primary Willow
/// #A3C4A8; text in Roaster (deep brown); subtle shadows; rounded cards.
/// Layout
/// SafeArea → Column (scrollable content above fixed bottom nav):
/// AppBar
/// * Title: Journal
/// * Right icon: search (opens JournalSearchModal—optional simple client-side
/// search)
/// Section A: Today’s Entry (Card)
/// * Row:
///     * Left: Large formatted date (e.g., “Thu, Aug 7”)
///     * Right: Mood Dropdown (Form Dropdown)
///         * Hint: “Mood”
///         * Items: Great, Good, Okay, Low, Drained
/// * Multiline TextField (min 6 lines, filled, rounded, placeholder “What’s
/// on your mind today?”)
/// * Row Buttons:
///     * Left: Save Draft (outlined, Willow border/text)
///     * Right: Save Entry (primary, Willow bg, white text)
/// * Tiny caption under buttons: “Auto-saves while you type.”
/// Behavior
/// * On load: compute todayStart = startOfDay(now). Query for a doc where
/// date == todayStart (limit 1). If found, prefill mood/text; primary button
/// label changes to Update Entry.
/// * Save Draft / Save Entry:
///     * If today exists: update text, mood, updatedAt = serverTimestamp.
///     * Else: create with date = todayStart, text, mood, createdAt,
/// updatedAt.
/// * Auto-save: TextField onChanged with 2–3s debounce → same upsert as Save
/// Draft.
/// Section B: History Filters (Row)
/// * Month Dropdown (names Jan–Dec mapped to 1–12)
/// * Year Dropdown (e.g., currentYear down to currentYear−10)
/// * Optional: Mood Filter Dropdown (All / Great / Good / Okay / Low /
/// Drained). Default “All”.
/// When Month/Year changes → re-run the month query.
/// Section C: History List (for selected Month/Year)
/// * Subheader: “Entries for {MonthName} {Year}”
/// * Paginated ListView of entries in date DESC
///     * Each item card:
///         * Top row: formatted date (“Aug 3, 2025”) • small “Mood: ___”
///         * Body: first 2–3 lines of text (truncate/fade)
///         * Right chevron → tap opens JournalDetailPageSimple
/// * Empty state if none: “No entries this month.”
